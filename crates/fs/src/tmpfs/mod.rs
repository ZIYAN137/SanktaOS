//! Tmpfs - 内存临时文件系统
//!
//! 该模块提供了一个**完全驻留在内存中的文件系统**，支持完整的 POSIX 语义。
//!
//! ## 设计要点
//!
//! - 数据以页为单位存储在物理内存中，读写不涉及块设备 I/O
//! - 支持最大容量限制（以 MB 传入，内部换算为页数；0 表示无限制）
//! - 文件数据采用“稀疏页”存储：未分配页读取为 0，写入时按需分配
//! - inode 号与已分配页数通过共享的 `TmpfsStats` 统一统计

mod inode;
mod tmpfs;

pub use inode::TmpfsInode;
pub use tmpfs::TmpFs;
